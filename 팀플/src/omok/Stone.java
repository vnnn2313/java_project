package omok;

public class Stone {
	private int a;
	private int b;
	private int color;

	public Stone(int a, int b, int color) {
		super();
		this.a = a;
		this.b = b;
		this.color = color;
	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}

	public int getColor() {
		return color;
	}

	public void setColor(int color) {
		this.color = color;
	}

	}
